"""
За допомогою функції filter() з котрежу слів відфільтрувати тільки ті,
 які є поліндромами (які однаково читаються в обидві сторони), регістр літер не враховувати.
inputdata = ('Країна', 'шалаш', 'Летел', 'вертольот', 'УЧУ', 'мем', 'мова')
"""

inputdata = ('Країна', 'шалаш', 'Летел', 'вертольот', 'УЧУ', 'мем', 'мова')


def condition(x):
    return x[::-1].upper() == x.upper()


filtered_tuple = tuple(filter(condition, inputdata))

print(filtered_tuple)
